#include <stdio.h>

int main(){
	/* kalau while Do biasanya di pakai kalau kita
		sudah mengetahui kondisi berhenti di eksekusi*/
	
	/***Kamus***/
	int i;
	
	/***Algoritma***/
	i=0;					//initialisasi nilai awal i
	while(i<=10){ 			//Program akan di eksekusi dari i=0 sampai i = 10
		printf("%d\n",i);
		i++;				//incremental (i akan di tambah 1)
	}
	
}